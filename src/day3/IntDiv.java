package day3;

public class IntDiv {
    public static void main(String[] args) {
        int a, b, c;
        a = 1;
        b = 10;
        c = a / b;// int / int = 0 (celoe)
        System.out.println(c);

        double d, e, f;
        d = 1;
        e = 10;
        f = d / e;// int / int = 0.1 (drobnoe)
        System.out.println(f);


    }
}
