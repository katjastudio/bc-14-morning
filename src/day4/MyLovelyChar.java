package day4;

public class MyLovelyChar {
    public static void main(String[] args) {
        char dollar = '$';
        System.out.println(dollar);
        String myDollar = "$";
        System.out.println(myDollar);

        char zahl = 100;//iz tablici!!!!
        char octal = '\u03A6';
        char tabulator = '\t';
        System.out.println(zahl);
        System.out.println(octal);
        System.out.println(tabulator + "h");
        char symboll = 934;
        System.out.println(symboll);

    }
}
